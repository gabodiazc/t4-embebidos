// Realiza l lecturas de aceleración y giroscopio
// en los ejes x, y, z
void lecture(SensorData *sd, int l) {
    // tmp: variable temporal donde se guarda la lectura del BMI. 
    // OJO son 8 bits de lectura
    // reg_intstatus: sensor status flags (Datasheet)
    uint8_t reg_intstatus = 0x03, tmp;

    // ACELERACIÓN
    // Memdir donde alojan datos de aceleración en x, y, z
    uint8_t addr_acc_x_lsb = 0x0C;
    uint8_t addr_acc_x_msb = 0x0D;
    uint8_t addr_acc_y_lsb = 0x0E;
    uint8_t addr_acc_y_msb = 0x0F;
    uint8_t addr_acc_z_lsb = 0x10;
    uint8_t addr_acc_z_msb = 0x11;

    // Variables a almacenar el dato crudo aceleración
    uint16_t acc_x;
    uint16_t acc_y;
    uint16_t acc_z;

    // GIROSCOPIO
    // Memdir donde alojan datos de giroscopio en x, y, z
    uint8_t addr_gyr_x_lsb = 0x12;
    uint8_t addr_gyr_x_msb = 0x13;
    uint8_t addr_gyr_y_lsb = 0x14;
    uint8_t addr_gyr_y_msb = 0x15;
    uint8_t addr_gyr_z_lsb = 0x16;
    uint8_t addr_gyr_z_msb = 0x17;

    // Variables a almacenar el dato crudo giroscopio
    uint16_t gyr_x;
    uint16_t gyr_y;
    uint16_t gyr_z;

    for (int i=0; i<l; i++) {
        // printf("iteración %d\n", i+1);

        // cuando un dato del acelerómetro es read out
        bmi_read(&reg_intstatus, &tmp, 1);

        if ((tmp & 0b10000000) == 0b10000000) {
            // DATA READY FOR ACCELEROMETER
            ret = bmi_read(&addr_acc_x_msb, &tmp, 1);
            acc_x = tmp;
            ret = bmi_read(&addr_acc_x_lsb, &tmp, 1);
            acc_x = (acc_x << 8) | tmp;

            ret = bmi_read(&addr_acc_y_msb, &tmp, 1);
            acc_y = tmp;
            ret = bmi_read(&addr_acc_y_lsb, &tmp, 1);
            acc_y = (acc_y << 8) | tmp;

            ret = bmi_read(&addr_acc_z_msb, &tmp, 1);
            acc_z = tmp;
            ret = bmi_read(&addr_acc_z_lsb, &tmp, 1);
            acc_z = (acc_z << 8) | tmp;

            // printf("acc_x: %f g\n", (float)((int16_t)acc_x * (8.000 / 32768)));
            // printf("acc_y: %f g\n", (float)((int16_t)acc_y * (8.000 / 32768)));
            // printf("acc_z: %f g\n\n", (float)((int16_t)acc_z * (8.000 / 32768)));

            sd->acc_x[i] = (float)((int16_t)acc_x * (8.000 / 32768));
            sd->acc_y[i] = (float)((int16_t)acc_y * (8.000 / 32768));
            sd->acc_z[i] = (float)((int16_t)acc_z * (8.000 / 32768));
        
            if (ret != ESP_OK) {
                // printf("Error lectura: %s \n", esp_err_to_name(ret));
            }
        }

        // Lo mismo que arriba, solo que ahora para el giroscopio
        // su bit READY es el bit 6
        bmi_read(&reg_intstatus, &tmp, 1);

        if ((tmp & 0b01000000) == 0b01000000) {
            // DATA READY FOR GYROSCOPE
            ret = bmi_read(&addr_gyr_x_msb, &tmp, 1);
            gyr_x = tmp;
            ret = bmi_read(&addr_gyr_x_lsb, &tmp, 1);
            gyr_x = (gyr_x << 8) | tmp;

            ret = bmi_read(&addr_gyr_y_msb, &tmp, 1);
            gyr_y = tmp;
            ret = bmi_read(&addr_gyr_y_lsb, &tmp, 1);
            gyr_y = (gyr_y << 8) | tmp;

            ret = bmi_read(&addr_gyr_z_msb, &tmp, 1);
            gyr_z = tmp;
            ret = bmi_read(&addr_gyr_z_lsb, &tmp, 1);
            gyr_z = (gyr_z << 8) | tmp;

            // TODO: hay que dejar bien la unidad de medida
            // printf("gyr_x: %f \n", (float)gyr_x);
            // printf("gyr_y: %f \n", (float)gyr_y);
            // printf("gyr_z: %f \n\n", (float)gyr_z);

            // printf("justo después de imprimir giroscopio iteración %d\n", i+1);

            sd->gyr_x[i] = (float)gyr_x;
            sd->gyr_y[i] = (float)gyr_y;
            sd->gyr_z[i] = (float)gyr_z;

            // printf("justo después de asignar variables en la memoria it %d\n", i+1);
            
            if (ret != ESP_OK) {
                // printf("Error lectura: %s \n", esp_err_to_name(ret));
            }

            // printf("justo después de chequear si hay error de lectura\n");
        }
    }

    // printf("antes de readline\n");
    return;
}

// Envía un OK al PC
void send_OK() {
    uart_write_bytes(UART_NUM, "OK\n", 4);
}

// Espera en busywait por un OK enviado desde el PC
void wait_OK(void) {
    char dataResponse[3];
    while (1) {
        int rLen = serial_read(dataResponse, 3);
        if (rLen > 0) {
            if (strcmp(dataResponse, "OK") == 0)
                break;
        }
    }
}

// Se queda esperando por una respuesta del PC y retorna
// en base a la respuesta que obtuvo:
// Retorna 1 para tomar lecturas con la ventana actual y enviar datos
// Retorna 2 para cambiar la ventana
// Retorna 3 para cerrar la conexión
// Retorna 4 para enviar el tamaño actual de la ventana
int wait_response(void) {
    // Waiting for an BEGINX (with X any number) to initialize data sending
    char dataResponse[7];
    while (1) {
        int rLen = serial_read(dataResponse, 7);
        if (rLen > 0 && strncmp(dataResponse, "BEGIN", 5) == 0) {
            send_OK();
            return dataResponse[5] - '0';
        }
    }
}

