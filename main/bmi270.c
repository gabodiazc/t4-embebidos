i2c_master_dev_handle_t device;

// Hace que el ESP reciba datos del sensor 
// mediante comunicación I2C. Se le pasa como parámetro
// la dirección de memoria donde hay que ir a buscar la
// información
esp_err_t bmi_read(uint8_t *data_address, uint8_t *data_rd, size_t size) {
    if (size == 0) {
        return ESP_OK;
    }
    esp_err_t ret;

    // Perform I2C read operation
    ret = i2c_master_transmit(device, data_address, 1, pdMS_TO_TICKS(1000));
    if (ret != ESP_OK) {
        return ret;
    }

    ret = i2c_master_receive(device, data_rd, size, pdMS_TO_TICKS(1000));
    return ret;
}

esp_err_t bmi_write(uint8_t *data_address, uint8_t *data_wr, size_t size) {
    esp_err_t ret;

    // Perform I2C write operation
    uint8_t full_data[1 + size];
    full_data[0] = *data_address;
    memcpy(&full_data[1], data_wr, size);

    ret = i2c_master_transmit(device, full_data, sizeof(full_data), pdMS_TO_TICKS(1000));
    return ret;
}

esp_err_t bmi_init() {
    esp_err_t ret;

    i2c_master_bus_config_t i2c_mst_config = {
        .clk_source = I2C_CLK_SRC_DEFAULT,
        .i2c_port = I2C_NUM_0,
        .scl_io_num = I2C_MASTER_SCL_IO,
        .sda_io_num = I2C_MASTER_SDA_IO,
        .glitch_ignore_cnt = 7,
        .flags.enable_internal_pullup = true,
    };

    i2c_master_bus_handle_t bus_handle;
    ESP_ERROR_CHECK(i2c_new_master_bus(&i2c_mst_config, &bus_handle));

    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address = ESP_SLAVE_ADDR,
        .scl_speed_hz = 100000,
    };

    // Add I2C device
    ret = i2c_master_bus_add_device(bus_handle, &dev_cfg, &device);
    return ret;
}

void chipid(void) {
    uint8_t reg_id = 0x00;
    uint8_t tmp;

    bmi_read(&reg_id, &tmp, 1);
    printf("Valor de Chip ID: %2X \n\n", tmp);
    if (tmp == 0x24) {
        printf("Chip reconocido.\n\n");
    }
    if (tmp != 0x24) {
        printf("Chip no reconocido. \nChip ID: %2x (deberia ser 0x24)\n\n", tmp);  // %2X
        exit(EXIT_SUCCESS);
    }
}

